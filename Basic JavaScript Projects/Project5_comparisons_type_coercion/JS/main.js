//Example of typeof operator returning "string"
document.write(typeof "Hello World");

//Example of coercion operator
document.write("10" + 5);

//Example of Infinity
document.write(2E310);

//Example of -Infinity
document.write(-3E400);

//Example of Boolean operator returning true
document.write(10 > 5);

//Example of Boolean operator returning false
document.write(11 < 6);

//Example of console log method
console.log(5 + 5);

//Console log method with boolean operator
console.log(5 >= 5);

//Console log method with double equals returning true
console.log(true == true);

//Console log method with double equals returning false
console.log(true == false);

//Example of triple equals returning true
document.write(5 === 5);

//Example of triple equals returning false with different data type and value
document.write(3 === "2");

//Example of triple equals returning false with different data type
document.write("4" === 4);

//Example of triple equals returning false with different value
document.write(2 === 3);

//Example of && AND operator returning true
document.write(2 < 3 && 4 > 3);

//Example of && AND operator returning false
document.write(2 > 3 && 4 < 3);

//Example of || OR operator returning true
document.write(2 < 3 || 4 > 3);

//Example of || OR operator returning false
document.write(2 > 3 || 4 < 3);

//Example of ! NOT operator returning false
document.write(!(2 < 3));

//Example of ! NOT operator returning true
document.write(!(2 > 3));